void main() {
  var nilai = 'A';
  switch (nilai) {
    case 'A':
      print('nilai terbaik');
    case 'B':
      print('nilai lumyan baik');
    case 'C':
      print('cukup baik');
    case 'd':
      print('tidak baik');
    case 'e':
      print('sangat buruk');
    default:
      print('anda bukan siswa');
  }
}
